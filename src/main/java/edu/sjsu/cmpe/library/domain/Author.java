package edu.sjsu.cmpe.library.domain;

public class Author {
	private int id;
	private String name;
	public Author()
	{
// 		Do Nothing		
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
